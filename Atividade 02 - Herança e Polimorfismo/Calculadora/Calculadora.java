package Calculadora;
public class Calculadora {

    public int somar(int a, int b){
        int somar = a + b;
        return somar;
    }
    public double somar(double a, double b){
        double somar = a + b;
        return somar;
    }
    public int somar(int a, int b, int c){
        int somar = a + b + c;
        return somar;
    }
}
